package com.riya.autoboxing;

public class AutoBoxing {

	public static void main(String[] args) {
		  int a = 10;          // primitive
	        Integer obj = a;    // autoboxing (int -> Integer)

	        System.out.println("Primitive value: " + a);
	        System.out.println("Wrapper object: " + obj);
	}

}
